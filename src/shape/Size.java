package shape;

/**
 * The type Size.
 */
public class Size {
  private double x;
  private double y;

  /**
   * Instantiates a new Size.
   *
   * @param x the x
   * @param y the y
   */
  public Size(double x, double y) {
    this.x = x;
    this.y = y;
  }

  /**
   * Gets y.
   *
   * @return the y
   */
  public double getY() {
    return y;
  }

  /**
   * Gets x.
   *
   * @return the x
   */
  public double getX() {
    return x;
  }

  /**
   * Sets x.
   *
   * @param x the x
   */
  public void setX(double x) {
    this.x = x;
  }

  /**
   * Sets .
   *
   * @param y the y
   */
  public void sety(double y) {
    this.y = y;
  }
}

